#include "../../includes/minishell.h"

int	is_builtin(char *cmd)
{
	if (!ft_strcmp(cmd, "cd"))
		return (1);
	return (0);
}

void	change_dir(char **args)
{
	chdir(args[1]);
}

void	execute_builtin(char **args)
{
	if (!ft_strcmp(args[0], "cd"))
	change_dir(args);
}